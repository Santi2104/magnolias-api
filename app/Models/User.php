 * @method static \Illuminate\Database\Eloquent\Builder|User whereUpdatedAt($value)
 * @method static \Illuminate\Database\Query\Builder|User withTrashed()
 * @method static \Illuminate\Database\Query\Builder|User withoutTrashed()
 * @mixin \Eloquent
 */
class User extends Authenticatable
{
    use HasApiTokens, HasFactory, Notifiable, SoftDeletes;

    /**
     * The attributes that are mass assignable.
     *
     * @var string[]
     */
    protected $fillable = [
        'name',
        'email',
        'password',
        'lastname',
        'dni',
        'edad',
        'nacimiento',
        'username',
        'role_id',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];



    /**
     * Get the role that owns the User
     *
     * @return \Illuminate\Database\Eloquent\Relations\BelongsTo
     */
    public function role()
    {
        return $this->belongsTo(Role::class);
    }

    /**
     * Get the coordinador associated with the User
     *
     * @return \Illuminate\Database\Eloquent\Relations\HasOne
     */
    public function coordinador()
    {
        return $this->hasOne(Coordinador::class, 'user_id');
    }

    /**
     * Get the afiliado associated with the User
     *
     * @return \Illuminate\Database\Eloquent\Relations\HasOne
     */
    public function afiliado()
    {
        return $this->hasOne(Afiliado::class);
    }

    /**
     * Get the vendedor associated with the User
     *
     * @return \Illuminate\Database\Eloquent\Relations\HasOne
     */
    public function vendedor()
    {
        return $this->hasOne(Vendedor::class);
    }

    /**
     * Get the administrativo associated with the User
     *
     * @return \Illuminate\Database\Eloquent\Relations\HasOne
     */
    public function administrativo()
    {
        return $this->hasOne(Administrativo::class);
    }

    public function UserRole(){
        return $this->role->id;
    }
}
